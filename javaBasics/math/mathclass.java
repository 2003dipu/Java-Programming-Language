public class mathclass {
    public static void main(String[] args) {
        double x = 3.14;
        double y = -9.98;
        double z = Math.max(x, y);
        System.out.println(z);
        double p = Math.min(x, y);
        System.out.println(p);

        double q = Math.sqrt(12);
        System.out.println(q);
    }
}