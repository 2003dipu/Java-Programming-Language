// for loop = executes a block of code a limited amount of times. Yeah you can control the execution of it with a condition

public class forLoop {
    public static void main(String[] args) {
        System.out.println("Project Name: For loops in Java");
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println("Countdown begins");
        for (int j = 10;j>=0;j--){
            System.out.println(j);
        }
        
    }
}
